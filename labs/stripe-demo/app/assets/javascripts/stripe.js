$(function(){

$('.stripe-button').bind('token', token_generated); //bind is a token event. bind means listen to the 'token' event that stripe created. when 'token' gets generated, call this function, 'token_generated'

});

function token_generated(e, token)
{
  $('#stripeform input[type=hidden]').val(token.id); //inserts into form the token generated by stripe javascript
  $('#stripeform').submit(); // javascript autosubmits the form!!!!!
}